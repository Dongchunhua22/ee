package com.example.dong;

public class Event {
    int max;
    int dangqian;

    public Event(int max, int dangqian) {
        this.max = max;
        this.dangqian = dangqian;
    }

    public int getMax() {
        return max;
    }

    public void setMax(int max) {
        this.max = max;
    }

    public int getDangqian() {
        return dangqian;
    }

    public void setDangqian(int dangqian) {
        this.dangqian = dangqian;
    }
}
